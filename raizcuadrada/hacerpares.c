void
hacerpares (int par[5], int r)
{
  int t = 0, t2 = 0, t3 = 0;
  par[0] = (r / 100000000);
  t3 = (r - ((r / 100000000) * 100000000));
  par[1] = (t3 / 1000000);
  t2 = (t3 - ((t3 / 1000000) * 1000000));
  par[2] = (t2 / 10000);
  t = (t2 - ((t2 / 10000) * 10000));
  par[3] = (t / 100);
  par[4] = (t - ((t / 100) * 100));
}
